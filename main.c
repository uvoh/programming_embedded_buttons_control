ButtonNumber GetButtonPressed() {
	ButtonNumber bn;
	bn = NOBUTTON;
	HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_SET);
//Now read the value of the four switches.
	if (HAL_GPIO_ReadPin(GPIOG, GPIO_PIN_13) == GPIO_PIN_RESET)
		bn = BUTTON4;
	else if (HAL_GPIO_ReadPin(GPIOG, GPIO_PIN_12) == GPIO_PIN_RESET)
		bn = BUTTON2;
	else if (HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_4) == GPIO_PIN_RESET)
		bn = BUTTON3;
	else if (HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_10) == GPIO_PIN_RESET)
		bn = BUTTON1;
	HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_RESET);
	if (HAL_GPIO_ReadPin(GPIOG, GPIO_PIN_13) == GPIO_PIN_RESET)
		bn = BUTTON8;
	else if (HAL_GPIO_ReadPin(GPIOG, GPIO_PIN_12) == GPIO_PIN_RESET)
		bn = BUTTON6;
	else if (HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_4) == GPIO_PIN_RESET)
		bn = BUTTON7;
	else if (HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_10) == GPIO_PIN_RESET)
		bn = BUTTON5;
	return bn;
}



void SetLEDcolor(LEDnumber led, LEDcolour Colour) {
	unsigned short Pattern;
	Pattern = Colour << led * 2;
	for (int i = 0; i < 16; i++) {
		if (Pattern << i & 0x8000) {
			HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, RESET);
		} else {
			HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, SET);
		}
		/* Do a clock pulse */
		HAL_GPIO_WritePin(GPIOG, GPIO_PIN_9, GPIO_PIN_RESET); // CLK_PIN
		HAL_GPIO_WritePin(GPIOG, GPIO_PIN_9, GPIO_PIN_SET); // CLK_PIN
	}
	HAL_Delay(1);
}
